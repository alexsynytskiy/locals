                                        I’d note that the calm energy that you are describing is in no way synonymous to consistency. To the contrary, the energy of that kind by its very nature resents being pinned down, being affixed determined meanings.
                                        <br>It occurred to me that artists might be living embodiments of such energy, since all their works come from a single source that does not run dry. They also destroy any reality that they chose, or, rather, undermine its stable foundations, setting it in motion. Otherwise they run the risk of being trapped by the system.
                                        <br>These days, as yet another political scaffolding is collapsing, I have to confront the question: can an artist cope with reality? Meaning, can art become a weapon? Probably not. But maybe it can become a useful tool.
