                                        <div class="speaker-name">
                                            V.B.
                                        </div>
                                        <div class="speaker-phrase">
                                            Poetic language is reality. We’ve focused on Shevchenko’s poetic language, but it’s in the nature of language as such, that is, not inane chatter but the poetic language, which offers the precise concentrated expression of our times. Without artistic language, reality disintegrates. Contained in artistic language, reality is preserved; it won’t ever drift into the past.
                                            <br>This way, Shevchenko’s poetic language – irrational, free of all affectation, forceful – offered the most precise expression of our present-day reality. To put a more forceful case, it encapsulates our present-day reality, as if Shevchenko himself came out to celebrate.
                                            <br>You are saying that some people gained powers to recast reality, but one cannot do that: reality can only be perceived, invented, mastered. Reality just is, once you look closely. For example, here I am, drawing a line, and it becomes the only reality for me: my reality is embodied in points where I “pull” the line to the surface or submerge it. This simple line, if drawn precisely, contains a treasure trove of information. The lines I draw have neither the past nor the present. When working, you create your own objects, you select them among the chimaeras of the real and embody them.
