                                    <div class="speaker-phrase">
                                        It’s worth noting that the essence always hides in between the events. For example, there’s a subtle yet staggering power in the constant, unwavering energy of the landscape that, if old pictures are to be trusted, hasn’t changed since Shevchenko’s time. Maybe we are fighting the war precisely to preserve this unwavering calm, like the one you see in this landscape.
                                        <br>Don’t you see, in the markedly simple lines of the landscape, that the air between the trees is pulsating with the war?
                                        <br>It’s the things that remain quiet even now that are the most stunning. They somehow undermine the restless reality of the last months to prove their authenticity. In times of war, these calm objects are brimming with war too: war glorifies and elevates them, because, in a certain perverse way, war is the most substantial proof of their existence and authenticity.
