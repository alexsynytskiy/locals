                        <div class="title">
                            Shelter
                        </div>
                        <div class="column" style="margin-right: 20px;">
                            A perilous rift is born of the ambiguity of war and peace, the abyss that the essence sinks into. It perishes while the world scatters in panic. In the spaces that we expected to offer us the salvation of values opens the abyss, an absence, an unsprung trap. We slip into the rift between wrongdoing and the crime of omission.
                            <br>The first year of the war might have passed, but that doesn’t make it any more normal. Time slows down and replicates, day in day out, its soggy banality. We are almost out of rage, fire and enthusiasm, with nothing but mounting exhaustion to replace them. We grow used to anxiety, yet numbness does little to protect. We grow used to grief, yet that doesn’t help either: on the contrary, grief accumulates, adds up, crystallizes.
                            <br>We got trapped in the time that goes stale and rots without changing. We are so worn down by anxiety that we want to escape not only the war, but also the peace that nurtures the war. Whence does danger come? Even peace is a danger, frayed as it is by the unwavering threat of a yet bigger war. Our chimaera of peace festers in borrowed time, and we’ll yet have to pay for it.
                            <br>When Bomb Shelter markings started cropping up on the walls in Kyiv, it seemed like a quaint anachronism, a thing straight out of civic defense classes in Soviet schools. Bomb shelters for air raids reminded us that we do still have a quantity of calm, set aside against a rainy day. It’s symbolic that occasionally the markings led nowhere, with not a shelter in sight.
                            <br>Shelter, the second part of our project, is defined by this search for a shelter in unexpected places, in emptiness (in silence).
                            <br>We were working on Poet’s Refuge in Kaniv last year, when the events were still unfolding with overwhelming speed. We explored whether poetic language can become a weapon, and what role an artist can play in times that can be described as extreme. We didn’t have time for redefinitions: the world was turning upside down right as we were watching. Everything was changing too fast. Everything seemed to brim with action.
                        </div>
                        <div class="column">
                            Now events seem to endlessly repeat themselves. We’ve spent the last year in a world whose outlines were washed away by this strange war; reality became muddled, ill defined. After familiar objects exhausted themselves and could no longer serve their functions, we lost our footing. The world wastes and wanes in mundanely devouring itself. It probably can devour itself in ambiguity, because the old familiar definitions no longer apply. Mundane language devolved to inane chatter, no longer fit to narrate or explain.
                            Artists, however, don’t have a choice but to keep talking to the world and to themselves, not that there’s much of a difference, because only language can shelter both.
                            <br>Outside of language, the world wastes away, but then, so does artistic language, if the world uses it in vain. (Doesn’t the empty imitation of language remind us of sham bomb shelter signs that imperil the lives of those who can’t tell language and non-language apart?)
                            <br>We should lead language back to silence, its temporary shelter.
                            <br>Only artistic language, artists’ haven and shelter, can lay bare the ambiguous world that we’ve found ourselves in. The language itself, however, also needs renewing, since words are depleted of meaning in mundane speech. Such shelters function as the silence and mysterious red lights of dark rooms, in which photographs emerge out of blank surfaces.
                            <br>Kaniv studios resemble both a dark room and a shelter. Working there feels like descending into an empty shelter, where speaking in silence runs parallel to restless reality. Therefore, we don’t have much of a choice but to try to slip back inside, into the primeval expanse and stillness, where ambiguity has not yet bred anxiety and indifference. To the contrary, it still harbours potential actions and an ability to discern, recognize and define the surrounding objects.

                            <div class="author">
                                Vlada Ralko, Volodymyr Budnikov, 2015
                            </div>
                        </div>
