                                        I elevated an axe on a pedestal as a paragon of just such a tool. I didn’t have to invent a thing. When I was staying in a village in the summer, it dawned on me that an axe, stuck vertically in a log, resembles monument on a pedestal. Maybe it’s a monument to Action or, rather, to Potential Action that can turn to Active Action at the least need. I painted many axes in Kaniv, in Poet’s Refuge. Of course, there was no working around “Shevchenko’s” axes in “The Skyline is Burning” or “The Route to Taras.” I think I glorified and elevated an axe, this very simple, primitive tool, in homage to Shevchenko’s poetry, where he pleaded that we turn “high” cult objects (such as aspergills or thuribles) into tools or weapons. In the poem that we quoted in the project, Shevchenko dethroned such sacred artifacts, while I put “Shevchenko’s” axe (“…hone your axe and break their slumber”) on a pedestal.
                                        <br>It is worth noting that I’m comparing an axe to a monument unironically. Irony means that you surrendered and gave up on approaching things. To put it more bluntly, irony defuses the world, cools it out. I wanted to show how heavy and dangerous this blade was, how quickly it could turn from a tool into a weapon, and vice versa. I encoded another meaning of an axe, a tool instrumental to the creation of primal symbols, in my drawing “Lullaby. I’m a little boy, I’ll climb up a pole…”
                                        <br>While we are on the subject of artistic language, one couldn’t dream of a better metaphor. Of course, artistic language has to be intelligible and honest.
                                        <br>But how can artistic language escape the limitations of harrowing times?
                                        Hence, another question that arose in the first days of our insurrection remains open to this day: how honest can an artist be without hurting those whom he supports as a citizen?
                                        <br>I have not yet had the time to reflect on that when mounting my White Pages exhibition, comprised of the works that open the Kyiv Diary cycle. Kyiv, the very centre of it, was maimed and burning, and I could think of nothing else. It was all too close, although I’m not sure if a distance emerged even after the eye of battle moved to the east. But still, in those horrifying days in February one wouldn’t think about editing out certain things. It seemed like everybody would understand everything.
                                        <br>In Kyiv, I was enthralled by this communion; in Kaniv, I met people who destroyed things or drifted away from reality. When staying in Poet’s Refuge, I drew giant people in certain recognizable locations around Chernecha Mountain. I made a conscious choice to depict the most mundane spaces: a zebra crossing, a bus stop, indistinct shrubbery lining the road, the headlights of a car on the road, a hill. People remain indistinct too: normal people, everymen in their day-to-day mundane world, yet drawn to a different scale. These giants roam among mundane objects that they can no longer use, because those became too small for them.
                                        <br>Little wonder that we focused on poetic language in a project that had a lot to do with the image of Shevchenko. Nobody communicates in poetry in his or her day-to-day life. One could say that my “giants” started speaking in poetry: this made them unsuited to mundane tasks, but instead they gained the gift of changing reality.
