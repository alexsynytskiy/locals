                                    <div class="speaker-name">
                                        V.B.
                                    </div>
                                    <div class="speaker-phrase">
                                        Artists often appear eerily calm, yet it does not necessarily connote indifference or the depths of immorality. They can explore the most horrifying things with an “eerie calm,” or notice the unspeakable tension in an ostensibly calm reality. They may evoke images from the past, no matter how distant or recent, to draw our attention to the essence of the here and now. The so-called urgency of such a perspective has nothing to do with the news. Moreover, the best publicized or the most stunning events don’t necessarily convey the essence; sometimes I find retreating to the periphery much more crucial.
