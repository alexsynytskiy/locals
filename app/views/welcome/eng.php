                                        The outskirts of Kaniv, next to the Chernecha Mountain, were just such a much-needed periphery. All the while, they also remained central to the Shevchenko mythos, an almost sacred symbol of Ukraine.
                                        <br>The tranquil, almost timeless energy of the Chernecha Mountain nurtured me through my work. Meanwhile, the year when we celebrated 200 years since Shevchenko’s birth left me somewhat perplexed: the destructive force of artificial actualization seemed to disperse any utterance about Shevchenko, or his words even, and to strip them of power.
                                        <br>It seemed like we were forced to evoke Shevchenko, tasked with referencing him. It became a rallying cry for collective atonement, or collective defusing of the overpowering, dangerous force of his poetry. Once pushed to the forefront of the news, Shevchenko became either repulsive or tamed.
                                        <br>Therefore, I had to avoid all images that we associate with Shevchenko, all images that were rammed to the forefront. I had more interest in the forces behind them, the invisible forces lurking in the darkness, not featured in any celebrations, seemingly unimportant. It didn’t mean that they were not important.
