                                <div class="phrase clearfix">
                                    <div class="speaker-name">
                                        V.B.
                                    </div>
                                    <div class="speaker-phrase">
                                        Artistic utterances are another sort of information altogether. Sure, you can grasp at “information” as your key word. Art definitely carries vital information, but does it have practical implications? Otherwise, what’s the use of such information? The answer is, everybody already knows the truths that artists voice. Or knew, anyway, and misplaced them as useless or not immediately applicable.
                                        <br>Something similar is happening to Shevchenko: he’s been ripped to quotes that can be used in any situation, because his poetry remains aggravating; you cannot twist it to fit your circumstances, it transcends the limitations of discrete situations. Poetic word is cut of a different cloth. It’s larger than life.
                                        <br>Speaking of Chernecha Mountain, where Shevchenko is buried: its slopes are an unusable landscape. Nobody lives on or around it. Shevchenko himself couldn’t have settled there. Nobody knows what to do about this iconic space. It seems to exist outside of our quotidian life, in another space entirely. All I’m saying is, it’s as if Shevchenko created the landscape by describing it. He created himself a house, but he couldn’t use it, couldn’t enter the house on the mountain that he dreamed into existence, because that house existed on another plane. You couldn’t find a better example: the poet eventually entered the world of his own making after his death.
                                        <br>The Kaniv reservoir is all too symbolic: it reconfigured the iconic landscape that Shevchenko dreamed up from his vantage point on Chernecha Mountain. The reservoir supports the Kaniv Hydroelectric Power Plant, that is, it helps to produce energy. Hence, the energy of the “howling and groaning” Dnieper, as Shevchenko described it, was yoked to quotidian needs. Quite possibly, the good deed destroyed the poetic energy of the iconic landscape: these days, it is dispersed over the changed landscape, marred by the shapes of artificial buildings.
                                        <br>In my works “Reservoir” and “By the Dnieper,” I concentrated on the waterline that seems pregnant with the energy that Shevchenko brought forth. Outwardly, the landscape is perfectly still, somnolent. It’s another kind of energy that cannot go to waste: it remains calm and steady, the kind of energy that doesn’t disperse, stays constant in its existence.
