                                    <div class="speaker-phrase">
                                        Could it be that art as a whole occasionally goes into sleep mode?
                                        <br>Warnings abound in works of art, but they are subtle, coded, unnoticeable when viewed in haste, distractedly, in between quotidian tasks. They are not instantly applicable, and, hence, artists never get the warnings across in time.
                                    </div>
