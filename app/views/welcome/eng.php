                                            <br>I saw the white expanse of a canvas subsume, “devour” my willows. I submerged the willows into canvases, moving towards the starting point. I was returning to the times before the landscape entered language, as if Shevchenko had not yet depicted the wind that “shakes the willows.” The works are square, the format limits our line of vision, prevents the gaze from drifting upwards or sideways. Frontal view urges us to grasp the last traces of depictions that disappear as we speak, to search for footing that doesn’t exist outside the void, the primal pure whiteness of space.
