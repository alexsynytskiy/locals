                                    <div class="speaker-phrase">
                                        What is an artist to do in a world that is turning upside down? We had to confront the question the very moment it all started. Do artists have to “hone” change or otherwise adjust their tools in order to stay in touch with the changed world? Everything was changing so fast that it seemed like we didn’t have a choice but to hasten too. Speaking for myself, I decided that staying silent or continuing on as if nothing had happened wasn’t appropriate, no more so than sticking exclusively to activism. Since the very first days of the protests in Ukraine, the events were unfolding, one might say, right in front of our eyes. Some crucial facts could be lost, garbled by time or interpretations, or even deliberately changed to provide a more acceptable and intelligible version of reality. I feared that some things could be lost or forgotten just because we didn’t have the distance or time for reflection.
                                    </div>
                                </div>
                                <div class="phrase clearfix">
                                    <div class="speaker-name">
                                        V.B.
                                    </div>
                                    <div class="speaker-phrase">
                                        Actually, I don’t think that artists should deliberately engage with the obvious changes in their artistic practices. Moreover, I’m suspicious of any such direct responses, even to such harrowing events as ours, because an artist cannot look at his or her subjects directly. Direct viewing is unreliable. Besides, how do you tell whether it’s the artist’s take on the event that is important and urgent, or the event itself? Should we hold artists hostage to such superficial urgency? If an artist tries to cover events before the media do and starts to spout op-eds, doesn’t that devalue art? Is there the one true and tried protocol for artists in times of great calamities?
                                        <br>I’m not saying that artists shouldn’t have opinions on matters of public importance, but when it comes to art, that’s not how these opinions should be expressed. Civic and artistic practices shouldn’t overlap. Maybe the artists whose works don’t evidence a marked shift just view the changed world from a different perspective? Maybe the world started to change way before the shift became evident?
                                        <br>Such lightning-fast artistic reactions can be viewed as catering to the expectations of the audience, wouldn’t you say? The events that happened in Kyiv and continue unfolding in the east are too complex. People get lost in their own ambiguity. The world became too unstable, uncertain, unsafe, and people don’t know how to react. Even the indolent indifference which they always believed to be safe failed to protect them. With so much grief, so many dead, and the abounding struck-down idols, they had to fill the vacant spaces in their heads, pronto. Like in “the good old days,” the artists of the ideologue persuasion are keen to hold up their end of the “social deal,” of the sort. Ring any bells?
                                        <br>It sometimes seems that, in times of war, artists are expected to instantly procure an explanation to the new realities, an updated panorama of Us versus Them.
                                    </div>
                                </div>
                                <div class="phrase clearfix">
                                    <div class="speaker-name">
                                        V.
                                    </div>
                                    <div class="speaker-phrase">
                                        Lately one often hears that “an artist doesn’t owe anybody a thing.” Tempting as it might sound, I find such sweeping generalizations overtly populist. Sure, an overly hasty artistic response to the recent events might elicit certain suspicions, but there’s no template for such reactions. An artist has the right to an instant “heated” response, much as he or she has the right to remain silent. Not to mention that silence, too, can bespeak a civic stance, defiance, even rage.
                                        <br>It’s the artists that face the multitude of challenges and potential pitfalls; their position is especially precarious, loaded and charged. Attempts at hasty generalizations about recent events, when there’s no critical distance yet, might yield a distorted picture. Such distortions, in their turn, warp artistic language until it’s stripped of all meaning.
                                        It might seem that the artists who are active these days, in times of war, continue on with their earlier projects. The impression persists despite outward changes in their works. Obviously, many of them sensed the coming shifts when everything still appeared calm.
                                        <br>When we worked on “Poet’s Refuge” in Kaniv, only snippets of conversations, like the mentions about a new wave of military drafts, reminded us about the war in the east. Our studios next to Chernecha Mountain were quiet, eerily quiet, I would even say. There was no telling how we should navigate around the jarring contrast between the horrifying news from the east and the immobile shapes of fishermen mirrored in the almost perfect stillness of the Dnieper. The ambiguity was uncanny: are the fishermen completely calm, or is the calm only skin-deep? Is the quiet landscape really calm? We’ve never experienced anything like that calm!
                                        <br>The war might have been distant, but there was no escaping it. Therefore, we had to select the elements of the peaceful and somnolent reality that best conveyed the impression of the war. But we only knew about the war from the news. How could we cope with the media that gradually turned the news into a theatre of cynicism? The war might have been distant, but the fact that we were a ten minutes’ walk away from Taras Shevchenko’s grave, and, thus, always close to the majestic and tragic poet, allowed us a special perspective.
                                        <br>By the way, Shevchenko wrote his long poem “Haidamaky” in Saint Petersburg, surrounded by high society; he himself marveled at the contrast between his carefree milieu and the riveting and cruel history that he was describing. Despite all that, he described the historical events as if he witnessed them. Hence, I’m not really sure who qualifies as a witness. In a way, Shevchenko might have been a true witness to the Cossack rebellion known as Koliyivshchyna that he described from a distance. His long poem “Haidamaky” offers indisputable proof of that. It can be read as testimony about those events, because certain crucial moments inevitably slip through the lines of even the most meticulously arranged documents, while artists can reconstruct them, pull them forward through the cracks of time, preserve them and bring them back to life. Some things either don’t fit documentary sources, or get corrupted in them. Works of art, meanwhile, can be the best repositories for the essential.
                                        <br>Do artists have to participate in events in order to depict them? None of this brings us any closer to answering this question. I think it’s a pressing question in times of war. I don’t yet have the answer, yet I thought that retreating back to my studio and continuing to work as if nothing had happened would have been immoral. In times of war, finding shelter from the real in a safe haven of a studio felt like treason. Staying a sheltered studio artist rather than an active citizen, activist, soldier felt like treason.
                                        Kaniv also felt like such a safe haven, eerily calm in its split from reality.
