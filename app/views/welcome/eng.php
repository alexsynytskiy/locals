                                    <div class="speaker-phrase">
                                        I would like to argue that avoiding the topic du jour can be as limiting as adhering to it. Obviously, when you are in the thick of battle, you cannot keep it at arm’s length.
                                        <br>When I was in the tranquil Kaniv, the situation changed somewhat, but not much. The events at the front lines unfolded so vertiginously fast that reality crushed any and all attempts to regain our bearing and make sense of it. However, the most eye-catching phenomena, the ones that washed up on the shore, so to speak, also direct us back to the depths. My artistic choices were determined by dense dramatic reality, the staggering shifts in the familiar world, the horrifying carnival of life and death. I had to grab this reality, whole chunks of it, the fragments of real time, without waiting to analyze or grasp the entirety of it.
                                        <br>While staying at Poet’s Refuge, I added several pages to the Kyiv Diary that I began back in the late 2013, as the Euromaidan protests were starting. By creating a multitude of drawings, a diary of a kind, I avoided generalizations, the pitfalls of “glorifying” or “idealizing.” It also allowed me to put down everything that was happening without editorializing, no matter how difficult or perverse.
